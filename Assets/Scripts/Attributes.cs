public class Attributes 
{
    private int _strength = 1;
    private int _agility = 1;
    private int _intelligence = 1;

    public int strength
    {
        get => _strength;
        set => _strength = value;
    }

    public int agility
    {
        get => _agility;
        set => _agility = value;
    }

    public int intelligence
    {
        get => _intelligence;
        set => _intelligence = value;
    }
}
